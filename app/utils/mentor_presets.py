def get_system_prompt(preset_id: int, review_mode: str = "socratic") -> str:
    base = MENTOR_PROMPTS.get(preset_id, MENTOR_PROMPTS[3])
    
    if review_mode == "socratic":
        base += " Используй исключительно сократический метод: не давай готовых решений, заставляй студента прийти к ответу самому через вопросы."
    elif review_mode == "direct":
        base += " Будь прямолинеен: сразу указывай, в какой строке ошибка и почему это плохо. Не трать время на загадки."
        
    return base
